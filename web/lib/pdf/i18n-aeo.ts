export function aeoLabel(lang: Lang, key: string): string {
  return STRINGS[lang]?.[key] ?? STRINGS.en[key] ?? key;
}

export function scoreRating(lang: Lang, score: number): string {
  if (score >= 80) return aeoLabel(lang, 'excellent');
  if (score >= 60) return aeoLabel(lang, 'good');
  if (score >= 40) return aeoLabel(lang, 'fair');
  return aeoLabel(lang, 'poor');
}
